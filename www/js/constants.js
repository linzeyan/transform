/**
 * Maps encoding variant keys to their group and label information
 */
export const encodingVariantMap = new Map();
export const allEncodingVariants = [];
encodingGroups.forEach((group) => {
    group.variants.forEach((variant) => {
        encodingVariantMap.set(variant.key, {
            group: group.label,
            label: variant.label,
        });
        allEncodingVariants.push({ ...variant, group: group.label });
    });
});

